#include <iostream>
using namespace std;

int main() {
    int num1, num2, sum;

    // Ask for input
    cout << "Enter the first number: ";
    cin >> num1;

    cout << "Enter the second number: ";
    cin >> num2;

    // Calculate sum
    sum = num1 + num2;

    // Output result
    cout << "The sum is: " << sum << endl;

    return 0;
}
